const ChildComponent = () => {
  return (
    <div>
      <p>I am the child</p>
    </div>
  );
};

class ParentComponent extends React.Component {
  constructor(props) {
    super(props);
  }
  render() {
    return (
      <div>
        <h1>I am the parent</h1>

        {/* child component rendered in place using custom html tag */}
        <ChildComponent />
      </div>
    );
  }
};
